let sequence = 0;

function materialize(module, record) {
  sequence += 1;
  const rawOptions = [...record.correct, ...record.wrong];
  const shift = sequence % rawOptions.length;
  const options = rawOptions.map((_, index) => rawOptions[(index + shift) % rawOptions.length]);
  const answer = options
    .map((option, index) => (record.correct.includes(option) ? index : -1))
    .filter((index) => index >= 0);
  return createQuestion({
    id: `I${String(sequence).padStart(3, "0")}`,
    level: "intermediate",
    module,
    type: record.correct.length > 1 ? "multiple" : "single",
    context: record.context,
    prompt: record.prompt,
    options,
    answer,
    explanation: record.explanation,
  });
}

export const intermediateQuestions = Object.freeze(
  modules.flatMap((module) => module.questions.map((question) => materialize(module.id, question))),
);
